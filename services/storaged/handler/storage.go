func createCompany(service CompanyUsecase) http.HandlerFunc {
	return func(w http.ResponseWriter, r *http.Request) {
		if r.Method != http.MethodPost {
			http.Error(w, "method not allowed", http.StatusMethodNotAllowed)
			return
		}

		var req presenter.CompanyRequest
		if err := json.NewDecoder(r.Body).Decode(&req); err != nil {
			http.Error(w, fmt.Sprintf("unable to decode request body, err=%v", err), http.StatusBadRequest)
			return
		}

		// Convert presenter request to entity
		hrData := entity.HR{
			Name:        req.HR.Name,
			Email:       req.HR.Email,
			Phone:       req.HR.Phone,
			Position:    req.HR.Position,
			LinkedInURL: req.HR.LinkedInURL,
		}

		companyDetailsData := entity.CompanyDetails{
			Website:     req.CompanyDetails.Website,
			Industry:    req.CompanyDetails.Industry,
			FoundedYear: req.CompanyDetails.FoundedYear,
			Size:        req.CompanyDetails.Size,
			Description: req.CompanyDetails.Description,
			Logo:        req.CompanyDetails.Logo,
		}

		driveDetailsData := entity.DriveDetails{
			Status:           entity.DriveStatus(req.DriveDetails.Status),
			ScheduledDate:    req.DriveDetails.ScheduledDate,
			NumberOfOffers:   req.DriveDetails.NumberOfOffers,
			NumberHired:      req.DriveDetails.NumberHired,
			RolesOffered:     req.DriveDetails.RolesOffered,
			MinCGPA:          req.DriveDetails.MinCGPA,
			EligibleBranches: req.DriveDetails.EligibleBranches,
			Notes:            req.DriveDetails.Notes,
		}

		company, err := entity.NewCompany(
			0, // ID will be generated by repository
			req.CompanyName,
			req.LastContacted,
			req.FollowUp,
			req.Remarks,
			req.TargetBranch,
			req.Packages,
			hrData,
			driveDetailsData,
			companyDetailsData,
			req.Approved,
			req.IsValidation,
			req.Location,
		)
		if err != nil {
			http.Error(w, fmt.Sprintf("unable to create company entity, err=%v", err), http.StatusBadRequest)
			return
		}

		// Set creator
		company.CreatedBy = req.CreatedBy

		companyID, err := service.CreateCompany(company)
		if err != nil {
			http.Error(w, fmt.Sprintf("unable to create company, err=%v", err), http.StatusInternalServerError)
			return
		}

		// Create a response
		response := presenter.CompanyResponse{
			ID:             companyID,
			CompanyName:    company.CompanyName,
			LastContacted:  company.LastContacted,
			FollowUp:       company.FollowUp,
			Packages:       company.Packages,
			Remarks:        company.Remarks,
			HR:             req.HR,
			TargetBranch:   company.TargetBranch,
			IsValidation:   company.IsValidation,
			Approved:       company.Approved,
			DriveDetails:   req.DriveDetails,
			CompanyDetails: req.CompanyDetails,
			Location:       company.Location,
			CreatedAt:      company.CreatedAt,
		}

		w.WriteHeader(http.StatusOK)
		if err := json.NewEncoder(w).Encode(response); err != nil {
			http.Error(w, fmt.Sprintf("unable to encode to JSON, err=%v", err), http.StatusInternalServerError)
			return
		}
	}
}

