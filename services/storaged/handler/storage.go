func importCompaniesFromCSV(service CompanyUsecase) http.HandlerFunc {
	return func(w http.ResponseWriter, r *http.Request) {
		if r.Method != http.MethodPost {
			http.Error(w, "method not allowed", http.StatusMethodNotAllowed)
			return
		}

		// Limit file size to 10MB
		r.ParseMultipartForm(10 << 20)

		// Get file from request
		file, _, err := r.FormFile("csvFile")
		if err != nil {
			http.Error(w, fmt.Sprintf("unable to get file from request, err=%v", err), http.StatusBadRequest)
			return
		}
		defer file.Close()

		// Get creator ID from form
		creatorID := r.FormValue("creatorID")
		if creatorID == "" {
			http.Error(w, "creator ID is required", http.StatusBadRequest)
			return
		}

		// Parse CSV
		reader := csv.NewReader(file)

		// Read header
		header, err := reader.Read()
		if err != nil {
			http.Error(w, fmt.Sprintf("unable to read CSV header, err=%v", err), http.StatusBadRequest)
			return
		}

		// Map header columns to indices
		headerMap := make(map[string]int)
		for i, column := range header {
			headerMap[strings.TrimSpace(strings.ToLower(column))] = i
		}

		// Required columns
		requiredColumns := []string{"company_name", "location", "target_branch"}
		for _, col := range requiredColumns {
			if _, exists := headerMap[col]; !exists {
				http.Error(w, fmt.Sprintf("missing required column: %s", col), http.StatusBadRequest)
				return
			}
		}

		// Parse records
		var companies []*entity.Company
		for {
			record, err := reader.Read()
			if err == io.EOF {
				break
			}
			if err != nil {
				continue // Skip invalid records
			}

			// Create company entity from CSV record
			company := &entity.Company{
				CompanyName:    getValue(record, headerMap, "company_name"),
				Location:       getValue(record, headerMap, "location"),
				TargetBranch:   getValue(record, headerMap, "target_branch"),
				Remarks:        getValue(record, headerMap, "remarks"),
				CreatedBy:      creatorID,
				CreatedAt:      time.Now(),
				UpdatedAt:      time.Now(),
				ApprovalStatus: "PENDING",
			}

			// Parse packages if available
			if idx, exists := headerMap["packages"]; exists && idx < len(record) {
				packages := strings.Split(record[idx], ",")
				for _, p := range packages {
					p = strings.TrimSpace(p)
					if p != "" {
						if packageValue, err := strconv.ParseFloat(p, 32); err == nil {
							company.Packages = append(company.Packages, float32(packageValue))
						}
					}
				}
			}

			// Set HR data if available
			company.HR = entity.HR{
				Name:     getValue(record, headerMap, "hr_name"),
				Email:    getValue(record, headerMap, "hr_email"),
				Phone:    getValue(record, headerMap, "hr_phone"),
				Position: getValue(record, headerMap, "hr_position"),
			}

			// Set company details if available
			company.CompanyDetails = entity.CompanyDetails{
				Website:  getValue(record, headerMap, "website"),
				Industry: getValue(record, headerMap, "industry"),
			}

			// Add to list if basic validation passes
			if company.CompanyName != "" && company.Location != "" && company.TargetBranch != "" {
				companies = append(companies, company)
			}
		}

		// Import companies to database
		importedIDs, err := service.ImportCompaniesFromCSV(companies)
		if err != nil {
			http.Error(w, fmt.Sprintf("unable to import companies, err=%v", err), http.StatusInternalServerError)
			return
		}

		response := map[string]interface{}{
			"status":      "success",
			"message":     fmt.Sprintf("Imported %d companies", len(importedIDs)),
			"importedIDs": importedIDs,
		}

		w.WriteHeader(http.StatusOK)
		if err := json.NewEncoder(w).Encode(response); err != nil {
			http.Error(w, fmt.Sprintf("unable to encode to JSON, err=%v", err), http.StatusInternalServerError)
			return
		}
	}
}

