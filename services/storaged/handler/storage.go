func updateCompany(service CompanyUsecase) http.HandlerFunc {
	return func(w http.ResponseWriter, r *http.Request) {
		if r.Method != http.MethodPut {
			http.Error(w, "method not allowed", http.StatusMethodNotAllowed)
			return
		}

		var req presenter.CompanyUpdateRequest
		if err := json.NewDecoder(r.Body).Decode(&req); err != nil {
			http.Error(w, fmt.Sprintf("unable to decode request body, err=%v", err), http.StatusBadRequest)
			return
		}

		// Get existing company
		company, err := service.GetCompanyByID(req.ID)
		if err != nil {
			http.Error(w, fmt.Sprintf("unable to get company, err=%v", err), http.StatusInternalServerError)
			return
		}

		// Update company details
		if req.CompanyName != "" {
			company.CompanyName = req.CompanyName
		}
		if req.LastContacted {
			company.LastContacted = true
		}
		if !req.FollowUp.IsZero() {
			company.FollowUp = req.FollowUp
		}
		if req.Remarks != "" {
			company.Remarks = req.Remarks
		}
		if req.TargetBranch != "" {
			company.TargetBranch = req.TargetBranch
		}
		if req.Location != "" {
			company.Location = req.Location
		}
		if len(req.Packages) > 0 {
			company.Packages = req.Packages
		}

		// Update HR details if provided
		if req.HR.Name != "" || req.HR.Email != "" || req.HR.Phone != "" {
			if req.HR.Name != "" {
				company.HR.Name = req.HR.Name
			}
			if req.HR.Email != "" {
				company.HR.Email = req.HR.Email
			}
			if req.HR.Phone != "" {
				company.HR.Phone = req.HR.Phone
			}
			if req.HR.Position != "" {
				company.HR.Position = req.HR.Position
			}
			if req.HR.LinkedInURL != "" {
				company.HR.LinkedInURL = req.HR.LinkedInURL
			}
		}

		// Update Company Details if provided
		if req.CompanyDetails.Website != "" || req.CompanyDetails.Industry != "" {
			if req.CompanyDetails.Website != "" {
				company.CompanyDetails.Website = req.CompanyDetails.Website
			}
			if req.CompanyDetails.Industry != "" {
				company.CompanyDetails.Industry = req.CompanyDetails.Industry
			}
			if req.CompanyDetails.FoundedYear > 0 {
				company.CompanyDetails.FoundedYear = req.CompanyDetails.FoundedYear
			}
			if req.CompanyDetails.Size != "" {
				company.CompanyDetails.Size = req.CompanyDetails.Size
			}
			if req.CompanyDetails.Description != "" {
				company.CompanyDetails.Description = req.CompanyDetails.Description
			}
			if req.CompanyDetails.Logo != "" {
				company.CompanyDetails.Logo = req.CompanyDetails.Logo
			}
		}

		// Update Drive Details if provided
		if req.DriveDetails.Status != "" {
			company.DriveDetails.Status = entity.DriveStatus(req.DriveDetails.Status)

			// Only update these fields if we're updating the drive status
			if !req.DriveDetails.ScheduledDate.IsZero() {
				company.DriveDetails.ScheduledDate = req.DriveDetails.ScheduledDate
			}
			if req.DriveDetails.NumberOfOffers > 0 {
				company.DriveDetails.NumberOfOffers = req.DriveDetails.NumberOfOffers
			}
			if req.DriveDetails.NumberHired > 0 {
				company.DriveDetails.NumberHired = req.DriveDetails.NumberHired
			}
			if len(req.DriveDetails.RolesOffered) > 0 {
				company.DriveDetails.RolesOffered = req.DriveDetails.RolesOffered
			}
			if req.DriveDetails.MinCGPA > 0 {
				company.DriveDetails.MinCGPA = req.DriveDetails.MinCGPA
			}
			if len(req.DriveDetails.EligibleBranches) > 0 {
				company.DriveDetails.EligibleBranches = req.DriveDetails.EligibleBranches
			}
			if req.DriveDetails.Notes != "" {
				company.DriveDetails.Notes = req.DriveDetails.Notes
			}
		}

		// Set validation flag if requested
		if req.IsValidation {
			company.IsDataValidated = true
		}

		// Handle approval based on system settings
		if common.CurrentSettings.ApprovalMode == "auto" {
			company.ApprovalStatus = "APPROVED"
		} else {
			company.ApprovalStatus = "PENDING"
		}

		// Save updates
		err = service.UpdateCompany(company)
		if err != nil {
			http.Error(w, fmt.Sprintf("unable to update company, err=%v", err), http.StatusInternalServerError)
			return
		}

		response := companyToResponse(company)

		w.WriteHeader(http.StatusOK)
		if err := json.NewEncoder(w).Encode(response); err != nil {
			http.Error(w, fmt.Sprintf("unable to encode to JSON, err=%v", err), http.StatusInternalServerError)
			return
		}
	}
}

